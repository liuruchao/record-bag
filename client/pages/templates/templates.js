  getTemplates() {
    util.showBusy('请求中...');
    var that = this
    var options = {
      ...config.option('template'),
      success(result) {
        util.showSuccess('请求成功完成');
        console.log('request success', result);
        that.setData({
          templates: JSON.stringify(result.data)
        })
      },
      fail(error) {
        util.showModel('请求失败', error);
        console.log('request fail', error);
      }
    };
    qcloud.request(options)
  },

